package net.cineplex.webmodel.stat;

public class CityVO {

	String city;
	String percent;
	
	public void setCity(String city){
		this.city=city;
	}
	public void setPercent(String percent){
		this.percent=percent;
	}
	public String getCity(){
		return city;
	}
	public String getPercent(){
		return percent;
	}
}
